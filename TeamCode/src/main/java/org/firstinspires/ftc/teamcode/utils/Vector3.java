package org.firstinspires.ftc.teamcode.utils;

public class Vector3 {
    private double x, y, z;

    public Vector3(double x, double y, double z){
        this.x = x;
        this.y = y;
    }

    public double getX(){
        return x;
    }
    public double getY(){
        return y;
    }
    public double getZ() { return z; }
}
